package day25_loops;

public class PintEvenOrOddNumbers {
    public static void main(String[] args) {
        for (int n = 1; n <= 100; n++){
            if (n%2 == 0){
                System.out.print(n+" ");
            }
        }for (int k = 0; k<= 100; k++){
            if (k%2 != 0){
                System.out.print(k+" ");
    }
}}}
